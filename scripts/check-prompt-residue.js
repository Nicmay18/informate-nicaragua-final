async function checkPromptResidue() {
  console.log('🔍 Buscando prompt residue en las últimas 30 noticias...\n');

  try {
    const snapshot = await db
      .collection('noticias')
      .orderBy('fecha', 'desc')
      .limit(30)
      .get();

    console.log(`📊 Analizando ${snapshot.docs.length} noticias...\n`);

    const affected = [];

    for (const doc of snapshot.docs) {
      const data = doc.data();
      const slug = data.slug || doc.id;
      const titulo = data.titulo || 'Sin título';
      const contenido = data.contenido || '';
      const resumen = data.resumen || '';

      const fullText = (resumen + ' ' + contenido).toLowerCase();

      const foundPhrases = SUSPICIOUS_PHRASES.filter(phrase => 
        fullText.includes(phrase.toLowerCase())
      );

      if (foundPhrases.length > 0) {
        affected.push({
          id: doc.id,
          slug,
          titulo,
          fecha: data.fecha?.toDate?.() || data.fecha,
          foundPhrases,
          count: foundPhrases.length
        });
      }
    }

    if (affected.length === 0) {
      console.log('✅ No se encontró prompt residue en las últimas 30 noticias.');
    } else {
      console.log(`⚠️  Se encontraron ${affected.length} noticias con prompt residue:\n`);
      affected.forEach((news, i) => {
        console.log(`${i + 1}. ${news.titulo}`);
        console.log(`   Slug: ${news.slug}`);
        console.log(`   Fecha: ${news.fecha}`);
        console.log(`   URL: https://nicaraguainformate.com/noticias/${news.slug}`);
        console.log(`   Frases sospechosas (${news.count}):`);
        news.foundPhrases.forEach(phrase => {
          console.log(`      - "${phrase}"`);
        });
        console.log('');
      });

      // Guardar reporte
      const fs = require('fs');
      fs.writeFileSync(
        'prompt-residue-report.json',
        JSON.stringify(affected, null, 2)
      );
      console.log('📄 Reporte guardado en prompt-residue-report.json');
    }

  } catch (error) {
    console.error('❌ Error:', error);
  } finally {
    await admin.app().delete();
  }
}

checkPromptResidue();
